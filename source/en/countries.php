      <h3 class="text-center">Needs are still considerate</h3>
      <p>Yet, one knows that cure rate can reach 80%, under some conditions :</p>
      <ul>
        <li>Early diagnosis of cancer</li>
        <li>Fast access to a competent team</li>
        <li>Initial assessment and initiation of the treatment made within a short timeframe</li>
        <li>Treatment, often long, completed with post-treatment surveillance, which implies support from families, development of relays near the home and support for the most vulnerable ones</li>
      </ul>
      <p>To meet these needs the project to accelerate the provision of effective structures in French-speaking sub-Saharan Africa is suggested, with an overall objective of enabling effective management of :</p>
      <ul>
        <li>At least 30% of children (or about 3,500 new cases / year) in sub-Saharan Africa with cancer by 2020.</li>
        <li>At least 60% (about 7 000 cases / year) in 2025.</li>
      </ul>
